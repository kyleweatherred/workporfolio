#  ► 3.  create / lookup the AAD group and assign the role (idempotent)
# ──────────────────────────────────────────────────────────────────────
for acl in "${!group_role_map[@]}"; do
  role="${group_role_map[$acl]}"

  # ── 3a) ensure the backing Entra ID group exists ───────────────────
  gid=$(az ad group show --group "$acl" --query id -o tsv 2>/dev/null || true)
  if [[ -z $gid ]]; then
    gid=$(az ad group create \
            --display-name "$acl" \
            --mail-nickname "$acl" \
            --query id -o tsv)
    sleep 20                                 # directory replication
  fi

  # ── 3b) subscription‑level role assignment (safe to re‑run) ────────
  az role assignment create \
        --assignee-object-id "$gid" \
        --role               "$role" \
        --scope              "$SUB_SCOPE" \
        --assignee-principal-type Group \
        --only-show-errors 2>/dev/null || true
done

